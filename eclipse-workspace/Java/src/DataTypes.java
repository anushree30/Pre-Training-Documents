
public class DataTypes {
	public static void main(String args[]) {
		byte a = 100;
		int decimal = 100000;
		char c = 'A';
		String s = "This is data types";
		long l = 100000L;
		double d = 123.4;
		float f = 234.5f;
		boolean b = true;
		
		System.out.println("byte  : " + a + "\n" + "integer-decimal : "+ decimal+ "\n" + "character : " + c+ "\n" + "String : " + s + "\n" +"Long : "+ l + "\n" + "Double : " + d + "\n" + "Float : " + f +  "\n" + "Boolean : " + b);
	   
	   }
}
